static unsigned int ConvertTkModeToGlut(GLenum type)
{
    unsigned int glutMode = 0;
    
    if (TK_IS_RGB(type)) {
        glutMode |= GLUT_RGB;
    } else {
        glutMode |= GLUT_INDEX;
    }
    
    if (TK_IS_DOUBLE(type)) {
        glutMode |= GLUT_DOUBLE;
    } else {
        glutMode |= GLUT_SINGLE;
    }
    
    if (TK_HAS_DEPTH(type)) {
        glutMode |= GLUT_DEPTH;
    }
    
    if (TK_HAS_STENCIL(type)) {
        glutMode |= GLUT_STENCIL;
    }
    
    if (TK_HAS_ACCUM(type)) {
        glutMode |= GLUT_ACCUM;
    }
    
    if (TK_HAS_ALPHA(type)) {
        glutMode |= GLUT_ALPHA;
    }
    
    return glutMode;
}

static void defaultDisplayFunc(void)
{
    if (DisplayFunc) {
        DisplayFunc();
    }
}

static void defaultReshapeFunc(int width, int height)
{
    if (ReshapeFunc) {
        ReshapeFunc(width, height);
    }
}

GLenum tkInitWindow(char *title)
{
    static int glutInitialized = 0;
    
    if (!glutInitialized) {
        int argc = 1;
        char *argv[] = {"program", NULL};
        glutInit(&argc, argv);
        glutInitialized = 1;
    }
    
    // Convertir le mode TK en mode GLUT
    unsigned int glutMode = ConvertTkModeToGlut(w.type);
    glutInitDisplayMode(glutMode);
    
    // Définir la position et la taille de la fenêtre
    glutInitWindowPosition(w.x, w.y);
    glutInitWindowSize(w.w, w.h);
    
    // Créer la fenêtre principale
    glutWindowMain = glutCreateWindow(title);
    
    if (glutWindowMain == 0) {
        fprintf(stderr, "Failed to create GLUT window!\n");
        return GL_FALSE;
    }
    
    // Gérer l'overlay si nécessaire
    if (TK_HAS_OVERLAY(w.type)) {
        // GLUT ne supporte pas directement les overlays comme X11
        // On peut créer une seconde fenêtre ou ignorer cette fonctionnalité
        fprintf(stderr, "Warning: Overlay planes not fully supported in GLUT port\n");
    }
    
    // Configurer les callbacks par défaut - OBLIGATOIRE pour GLUT
    glutDisplayFunc(defaultDisplayFunc);
    glutReshapeFunc(defaultReshapeFunc);
    
    drawAllowFlag = GL_TRUE;
    
    return GL_TRUE;
}

