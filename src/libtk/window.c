void tkCloseWindow(void)
{
    if (glutWindowMain != 0) {
        cursorNum = 0;

        ExposeFunc = 0;
        ReshapeFunc = 0;
        IdleFunc = 0;
        DisplayFunc = 0;
        KeyDownFunc = 0;
        MouseDownFunc = 0;
        MouseUpFunc = 0;
        MouseMoveFunc = 0;

        glFlush();
        glFinish();
        
        if (TK_HAS_OVERLAY(w.type) && glutWindowOverlay != 0) {
            glutDestroyWindow(glutWindowOverlay);
            glutWindowOverlay = 0;
        }
        
        glutDestroyWindow(glutWindowMain);
        glutWindowMain = 0;
    }
}
